a=20
b=30

print("And Operator:-")
print("\n",a==b and a<=b) #False
print("\n",a<=b and a<b) #True 
print("\n",a==b and a>=b) #False

print("\nOr Operator:-")
print("\n",a==b or a<=b) #True
print("\n",a<=b or a<b) #True 
print("\n",a==b or a>=b) #False

print("\nNot Operator:-")
print("\n",not(a==b)) #True
print("\n",not( a<=b)) #False
